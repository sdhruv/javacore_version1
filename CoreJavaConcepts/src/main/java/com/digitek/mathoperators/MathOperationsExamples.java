package com.digitek.mathoperators;

public class MathOperationsExamples {

	public static void main(String[] args) {
		
		addition();
		subtraction();
		multiplication();
		division();
		
		
		
	}

	public static void addition() {
		
		int a = 10;
		int b = 20;
		
		int result = a + b;
		System.out.println("Value of addittion:" + result);
		
		
	}

	public static void subtraction() {
		
		int a = 10;
		int b = 20;
		
		int result = a - b;
		System.out.println("Value of subtraction:" + result);
		
		
	}
	
	public static void multiplication() {
		
		int a = 10;
		int b = 20;
		
		int result = a * b;
		System.out.println("Value of multiplication:" + result);
		
		
	}
	
	public static void division() {
		
		int a = 20;
		int b = 10;
		
		int result = a / b;
		System.out.println("Value of division:" + result);
		
		
	}
	
}




